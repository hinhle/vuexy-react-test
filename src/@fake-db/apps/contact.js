// ------------------------------------------------
// GET: Return Contact List
// ------------------------------------------------
mock.onGet('/apps/contact/contacts').reply(config => {
    // eslint-disable-next-line object-curly-newline
    const { q = '', perPage = 10, page = 1, status = null, rating } = config
    /* eslint-enable */
  
    const queryLowered = q.toLowerCase()
    const filteredData = data.contacts
      .filter(
        contact =>
          /* eslint-disable operator-linebreak, implicit-arrow-linebreak */
          (contact.name.toLowerCase().includes(queryLowered) ||
            contact.phone.toLowerCase().includes(queryLowered)) &&
            (!rating || contact.rating == parseInt(rating))
      )
      .sort(sortCompare('id'))
      .reverse()
    /* eslint-enable  */
  
    return [
      200,
      {
        allData: data.contacts,
        contacts: paginateArray(filteredData, perPage, page),
        total: filteredData.length
      }
    ]
  })